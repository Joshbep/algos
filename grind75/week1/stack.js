//stacks and overflows
//The last in, first out principle (LIFO) refers to the rule
//that whichever element is the last one to go into the stack
//will be the very first one that has to go out.

//Conversely, this also means that the first element
//that you put into the stack will always end up being
//the last one that goes out.

